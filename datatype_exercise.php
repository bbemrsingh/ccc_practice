<?php
//1. IntegerRepresents whole numbers without a decimal point.
    $a = 42;   
    echo "<br>";    
    var_dump($a); 

//2. Float Represents numbers with a decimal point.
    $b = 3.14;
    echo "<br>";
    var_dump($b);

//3. String Represents a sequence of characters.
    $strVar = "Hello, PHP!";
    echo "<br>";
    var_dump($strVar);


//4. Boolean Represents either `true` or `false`.
    $boolVar = true;
    echo "<br>";
    var_dump($boolVar);


//5. Array Represents an ordered map that can hold multiple values.
    $arrVar = array(1, 2, 3, "PHP");
    echo "<br>";
    var_dump($arrVar);
    

//6. NULL Represents the absence of a value or a variable without a value.
    $nullVariable = null;
    echo "<br>";
    var_dump($nullVariable);




?>